         <p><b>POSITION SUMMARY</b> </br></p>
        <p>The director is responsible for the successful leadership and management of the organization in accordance with the strategic direction approved by the Board of Trustees.</p>
        <p>Responsibilities include: Actively pursuing funding opportunities, hiring and providing supervision and support for the senior management team. Serving as the principle representative, fostering relationships and promoting collaboration with the public, donors and government agencies.</p>
        <p>The director plays a strategic development and leadership role in proactively identifying organizational needs, capacities and resources; exploring new opportunities to increase and expand its organizational capacity and maintaining fiduciary responsibility.</p>
        <p>Our mission is driven by our Faith in Jesus. We provide Quality Holistic Care that Creates Lasting Transformation, enabling Children, their families and community to flourish. We nurture the dreams of orphans and vulnerable children by providing them with a safe home, education and healthcare. we are partnering with local authorities to improve individual and community health through our medical center. We operate youth and women empowerment and advocacy programs.</p>
        <p>We need to keep children safe so our selection process reflects our commitment to the protection of children from abuse. This position is subject to our Child protection standards including background checks and adherence to our Child Safeguarding Policy. Excellent references will be required.</p>
        <p>This position provides a dynamic opportunity for and experienced missionary individual who is passionate about the Ministry to children and their communities. The organization has a friendly work environment, collaborative teams and provides a volunteer space for any Tanzanian national with a calling to serve these beautiful children and their community. </p>

